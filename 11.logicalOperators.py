# and or not are logical operators = used on codnitional statements

"""and"""
temp = 25
if temp>0 and temp<30:
    print("The temperature is good")
else:
    print("The temperature is bad")



"""or"""
temp = 25
if temp<=0 or temp>=30:
    print("The temperature is good")
else:
    print("The temperature is bad")



